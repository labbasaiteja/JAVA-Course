public class Day3 {
    public static void main(String[] args){
        int a = 6;
        int b = 2;
        int max;
        System.out.println("Welcome to World of Operators");
        System.out.println((a!=b)||(a==b));//logical operators
        max = a>b?a:b;
        System.out.println(a<<b);
        System.out.println("max value is "+max);
    }
}
